#include <bits/stdc++.h>
using namespace std;

int main() {
    int n;
    cin>>n;
    map<int,int> m;
    int a;
    for(int i = 0; i < n; i++) {
        cin>>a;
        m[a]++;
    }
    int cnt = 0;
    for(map<int,int>::iterator itr = m.begin(); itr != m.end(); itr++) {
        if(itr->second == 1) cnt++;
    }
    cout<<cnt;



    return 0;




}
